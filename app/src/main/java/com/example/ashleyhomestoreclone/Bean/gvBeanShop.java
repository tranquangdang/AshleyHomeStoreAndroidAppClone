package com.example.ashleyhomestoreclone.Bean;

public class gvBeanShop {
    int img;
    String cate;

    public gvBeanShop(int img, String cate) {
        this.img = img;
        this.cate = cate;
    }

    public int getImg() {
        return img;
    }

    public void setImg(int img) {
        this.img = img;
    }

    public String getCate() {
        return cate;
    }

    public void setCate(String cate) {
        this.cate = cate;
    }
}
